vim.cmd "hi St_relativepath guifg=#dbe0ee guibg=#2e2e3e"

M.ui = {
  statusline = {
    theme = "default",
    separator_style = "default",
    order = { "mode", "relativepath", "file", "git", "%=", "lsp_msg", "%=", "diagnostics", "lsp", "cwd", "cursor" },

    modules = {
      relativepath = function()
        local path = vim.api.nvim_buf_get_name(0)
        if path == "" then
          return ""
        end

        local relative_path = vim.fn.expand "%:.:h"
        if relative_path == "" then
          relative_path = "."
        end

        return "%#St_relativepath#" .. relative_path .. " /"
      end,
    },
  },
}

-- Изменяем цвет ASCII текста
vim.api.nvim_create_autocmd("VimEnter", {
  callback = function()
    vim.api.nvim_set_hl(0, "NvDashAscii", { fg = "#888BA6" })
    vim.api.nvim_set_hl(0, "NvDashButtons", { fg = "#8be9fd" })
  end,
})

-- Быстрый запуск .py файла
vim.api.nvim_create_user_command("Run", function()
  local ft = vim.bo.filetype
  local cmd

  if ft == "python" then
    cmd = "vsp | terminal python3 " .. vim.fn.expand "%"
  elseif ft == "sh" then
    cmd = "vsp | terminal bash " .. vim.fn.expand "%"
  else
    print("Unsupported filetype: " .. ft)
    return
  end

  vim.cmd(cmd)
end, {})

-- Переключение диагностики
vim.keymap.set("n", "<leader>df", function()
  local bufnr = vim.api.nvim_get_current_buf()
  if vim.diagnostic.is_disabled(bufnr) then
    vim.diagnostic.enable(bufnr)
    print "Diagnostics enabled"
  else
    vim.diagnostic.disable(bufnr)
    print "Diagnostics disabled"
  end
end, { desc = "Toggle LSP diagnostics" })

return M
